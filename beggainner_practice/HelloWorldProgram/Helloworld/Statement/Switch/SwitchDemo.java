package Statement.Switch;
public class SwitchDemo {
    public static void main(String[] args){
        for(int i=0; i<=100; i++){
          switch(i){
            case 0:
                System.out.println("i is zero ");
                break;
            case 1:
                System.out.println("i is one");
                break;
            case 2:
               System.out.println("i is two");
               break;
            case 3:
               System.out.println("i is three");
               break;
            case 4:
               System.out.println("i is four");
               break;
            case 5:
              System.out.println("i is five");
              break;
            case 6: 
              System.out.println("i is six");
              break;
            case 7:
              System.out.println("i is seven");
              break;
            case 8: 
              System.out.println("i is eight");
              break;
            case 9:
              System.out.println("i is nine");
              break;
            case 10: 
               System.out.println("i is ten");
               break; 
            default:
              System.out.println("i is " + i );
              break;              
                          

          }
        }
    }
    
}
